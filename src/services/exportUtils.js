  /**
   * Generate Markdown from test plan
   */
  static generateMarkdown(testPlan, issueData) {
    const sections = [
      `# Test Plan: ${testPlan.issueKey}`,
      '',
      '## Issue Summary',
      `**Key:** ${testPlan.issueKey}`,
      `**Summary:** ${issueData.summary}`,
      `**Type:** ${issueData.type}`,
      `**Priority:** ${issueData.priority}`,
      `**Status:** ${issueData.status}`,
      '',
      '## Overview',
      `- **Total Test Cases:** ${this.countTestCases(testPlan)}`,
      `- **Generated At:** ${testPlan.generatedAt}`,
      `- **Generated By:** GROQ (openai/gpt-oss-120b)`,
      '',
      '---',
      '',
    ];

    // Add each section
    const sectionOrder = [
      { key: 'positiveScenarios', title: 'Positive Scenarios' },
      { key: 'negativeScenarios', title: 'Negative Scenarios' },
      { key: 'edgeCases', title: 'Edge Cases' },
      { key: 'securityTests', title: 'Security Tests' },
      { key: 'performanceTests', title: 'Performance Tests' },
    ];

    for (const section of sectionOrder) {
      sections.push(`## ${section.title}`);
      sections.push('');

      const testCases = testPlan.testPlan[section.key] || [];
      
      for (const testCase of testCases) {
        sections.push(`### ${testCase.id}: ${testCase.title}`);
        sections.push('');
        sections.push(`**Description:** ${testCase.description}`);
        sections.push('');

        if (testCase.steps && Array.isArray(testCase.steps)) {
          sections.push('**Steps:**');
          for (let i = 0; i < testCase.steps.length; i++) {
            sections.push(`${i + 1}. ${testCase.steps[i]}`);
          }
          sections.push('');
        }

        sections.push(`**Expected Result:** ${testCase.expectedResult}`);
        
        if (testCase.priority) {
          sections.push(`**Priority:** ${testCase.priority}`);
        }

        sections.push('');
        sections.push('---');
        sections.push('');
      }
    }

    // Add metadata
    if (testPlan.metadata) {
      sections.push('## Metadata');
      sections.push('');
      
      if (testPlan.metadata.totalTestCases) {
        sections.push(`**Total Test Cases:** ${testPlan.metadata.totalTestCases}`);
      }
      
      if (testPlan.metadata.riskAreas && testPlan.metadata.riskAreas.length > 0) {
        sections.push('**High-Risk Areas:**');
        for (const area of testPlan.metadata.riskAreas) {
          sections.push(`- ${area}`);
        }
        sections.push('');
      }

      if (testPlan.metadata.assumptions && testPlan.metadata.assumptions.length > 0) {
        sections.push('**Assumptions:**');
        for (const assumption of testPlan.metadata.assumptions) {
          sections.push(`- ${assumption}`);
        }
        sections.push('');
      }
    }

    return sections.join('\n');
  }


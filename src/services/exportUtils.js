  /**
   * Generate test cases HTML
   */
  static generateTestCasesHtml(testPlan) {
    const sections = [
      { key: 'positiveScenarios', title: 'Positive Scenarios', icon: '✓' },
      { key: 'negativeScenarios', title: 'Negative Scenarios', icon: '✗' },
      { key: 'edgeCases', title: 'Edge Cases', icon: '⚡' },
      { key: 'securityTests', title: 'Security Tests', icon: '🔒' },
      { key: 'performanceTests', title: 'Performance Tests', icon: '⚙' },
    ];

    let html = '';

    for (const section of sections) {
      const testCases = testPlan.testPlan[section.key] || [];
      
      html += `<h2>${section.icon} ${section.title}</h2>`;
      
      for (const testCase of testCases) {
        const priorityClass = `priority-${(testCase.priority || 'medium').toLowerCase()}`;
        
        html += `
        <div class="test-case">
          <div class="test-case-header">${testCase.id}: ${testCase.title}</div>
          <div class="test-case-field">
            <span class="test-case-label">Description:</span>
            ${testCase.description}
          </div>
        `;

        if (testCase.steps && Array.isArray(testCase.steps)) {
          html += `
          <div class="test-case-field">
            <span class="test-case-label">Steps:</span>
            <ol class="steps-list">
          `;
          for (const step of testCase.steps) {
            html += `<li>${step}</li>`;
          }
          html += `
            </ol>
          </div>
          `;
        }

        html += `
          <div class="test-case-field">
            <span class="test-case-label">Expected Result:</span>
            ${testCase.expectedResult}
          </div>
        `;

        if (testCase.priority) {
          html += `
          <div class="test-case-field">
            <span class="test-case-label">Priority:</span>
            <span class="priority ${priorityClass}">${testCase.priority}</span>
          </div>
          `;
        }

        html += `</div>`;
      }
    }

    return html;
  }

  /**
   * Generate metadata HTML
   */
  static generateMetadataHtml(testPlan) {
    if (!testPlan.metadata) return '';

    let html = '<h2>Metadata & Analysis</h2>';

    if (testPlan.metadata.riskAreas && testPlan.metadata.riskAreas.length > 0) {
      html += '<h3>High-Risk Areas</h3><ul>';
      for (const area of testPlan.metadata.riskAreas) {
        html += `<li>${area}</li>`;
      }
      html += '</ul>';
    }

    if (testPlan.metadata.assumptions && testPlan.metadata.assumptions.length > 0) {
      html += '<h3>Assumptions</h3><ul>';
      for (const assumption of testPlan.metadata.assumptions) {
        html += `<li>${assumption}</li>`;
      }
      html += '</ul>';
    }

    return html;
  }

  /**
   * Download file helper
   */
  static downloadFile(content, filename, mimeType = 'text/plain') {
    const blob = new Blob([content], { type: mimeType });
    const url = URL.createObjectURL(blob);
    const link = document.createElement('a');
    link.href = url;
    link.download = filename;
    document.body.appendChild(link);
    link.click();
    document.body.removeChild(link);
    URL.revokeObjectURL(url);
  }

  /**
   * Count test cases
   */
  static countTestCases(testPlan) {
    let count = 0;
    const sections = [
      'positiveScenarios',
      'negativeScenarios',
      'edgeCases',
      'securityTests',
      'performanceTests',
    ];

    for (const section of sections) {
      if (testPlan.testPlan[section]) {
        count += testPlan.testPlan[section].length;
      }
    }

    return count;
  }

  /**
   * Copy to clipboard helper
   */
  static async copyToClipboard(text) {
    try {
      await navigator.clipboard.writeText(text);
      return true;
    } catch (error) {
      console.error('Failed to copy:', error);
      return false;
    }
  }
